def load_local_env() -> dict[str, str]:
    """
    Load environment variables from elastic-start-local/.env and .env.local.

    Reads ``.env`` for static defaults, then ``.env.local`` for runtime
    overrides (e.g. the API key regenerated by ``local-stack.sh``).
    Expands ``${VAR}`` and ``${VAR:-default}`` references, then injects
    into ``os.environ`` via ``setdefault`` so explicit env vars win.

    :return: Merged dictionary of environment variables
    """
    env_vars = {}

    env_dir = Path(__file__).parent.parent / "elastic-start-local"

    # Read .env (static defaults), then .env.local (runtime overrides)
    for filename in (".env", ".env.local"):
        env_file = env_dir / filename
        if env_file.exists():
            with open(env_file) as f:
                for line in f:
                    line = line.strip()
                    if line and not line.startswith("#") and "=" in line:
                        key, value = line.split("=", 1)
                        # Strip surrounding quotes (single or double)
                        if (
                            len(value) >= 2
                            and value[0] == value[-1]
                            and value[0] in ('"', "'")
                        ):
                            value = value[1:-1]
                        env_vars[key] = value

    # Second pass: expand variable references
    for key, value in env_vars.items():
        env_vars[key] = _expand_variables(value, env_vars)

    # Inject into os.environ so all downstream os.getenv() calls see these values.
    # setdefault preserves any variable already set in the environment.
    for key, value in env_vars.items():
        os.environ.setdefault(key, value)

    return env_vars


def _expand_variables(value: str, env_vars: dict[str, str]) -> str:
    """
    Expand variable references in a string.

    Supports:
    - ${VAR_NAME} - Expands to the value of VAR_NAME
    - ${VAR_NAME:-default} - Uses default if VAR_NAME is not set

    :param value: String that may contain variable references
    :param env_vars: Dictionary of available variables
    :return: String with variables expanded
    """

    def replace_var(match):
        var_expr = match.group(1)

        # Check for default value syntax: VAR_NAME:-default
        if ":-" in var_expr:
            var_name, default_value = var_expr.split(":-", 1)
            return env_vars.get(var_name, default_value)
        else:
            # Simple variable reference
            var_name = var_expr
            return env_vars.get(var_name, match.group(0))

    try:
        expanded_value = re.sub(r"\$\{([^}]+)\}", replace_var, value)
        return expanded_value
    except Exception as e:
        logger.warning(f"Failed to expand variables in '{value}': {e}")
        return value


# Inject .env variables into os.environ at import time so integration tests
