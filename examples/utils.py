def demonstrate_logger_selection():
    """
    Demonstrate logger selection for log forwarding.

    Shows how different loggers can be configured for forwarding.
    """
    print("=== Logger Selection Example ===")

    config = get_otel_config()
    configured_loggers = config.get("logs_loggers", [])

    print(f"Configured loggers for forwarding: {', '.join(configured_loggers)}")
    print("Demonstrating different logger usage:")
    print()

    # Demonstrate different loggers
    loggers_to_test = [
        ("kibana", "Main kibana client logger"),
        ("kibana.examples", "Examples logger"),
        ("kibana.actions", "Actions API logger"),
        ("kibana.saved_objects", "Saved Objects API logger"),
        ("my_app", "Custom application logger"),
        ("my_app.module", "Custom module logger"),
    ]

    for logger_name, description in loggers_to_test:
        test_logger = logging.getLogger(logger_name)
        forwarded = logger_name in configured_loggers or any(
            logger_name.startswith(configured) for configured in configured_loggers
        )

        status = "✅ FORWARDED" if forwarded else "❌ NOT FORWARDED"

        test_logger.info(
            f"Test message from {logger_name}",
            extra={
                "logger_demo": True,
                "logger_name": logger_name,
                "will_be_forwarded": forwarded,
            },
        )

        print(f"  {logger_name}: {description} - {status}")

    print()
    print(
        "💡 Configure KIBANA_OTEL_LOGS_LOGGERS environment variable to change logger selection"
    )
    print("   Example: KIBANA_OTEL_LOGS_LOGGERS=kibana,my_app")
    print()


