def demonstrate_log_trace_correlation():
    """
    Demonstrate log-trace correlation when both tracing and log forwarding are enabled.

    Shows how logs automatically include trace and span IDs for correlation in your observability backend.
    """
    try:
        from kibana.observability import span_context
    except ImportError:
        print("⚠️  OpenTelemetry not available - trace correlation not possible")
        return

    print("=== Log-Trace Correlation Example ===")

    # Use span_context to create a span that is set as the current span,
