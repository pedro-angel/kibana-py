load_local_env()


def get_kibana_config() -> tuple[str, tuple[str, str] | None, str | None]:
    """
    Get Kibana configuration from environment or local .env file.

    :return: Tuple of (kibana_url, basic_auth, api_key)
    """
    # Environment variables are populated from elastic-start-local/.env
    # at import time (via load_local_env), so os.getenv is the single lookup.
    kibana_url = os.getenv("KIBANA_URL") or os.getenv(
        "KIBANA_LOCAL_URL", "http://localhost:5601"
    )
    kibana_username = os.getenv("KIBANA_USERNAME")
    kibana_password = os.getenv("KIBANA_PASSWORD") or os.getenv("KIBANA_LOCAL_PASSWORD")
    api_key = os.getenv("KIBANA_API_KEY") or os.getenv("ES_LOCAL_API_KEY")

    # Prepare authentication
    basic_auth = None
    if kibana_username and kibana_password:
        basic_auth = (kibana_username, kibana_password)

    return kibana_url, basic_auth, api_key


def get_otel_config() -> dict[str, Any]:
    """
    Get OpenTelemetry configuration for OTLP integration.

    Configuration precedence:
    1. Environment variables (KIBANA_OTEL_*, OTEL_*)
    2. elastic-start-local/.env file
    3. Default values

    Returns:
        Dictionary containing:
        - enabled: bool
        - service_name: str
        - endpoint: str
        - headers: dict[str, str]
        - protocol: str
        - resource_attributes: dict[str, str]
        - logs_enabled: bool
        - logs_level: str
        - logs_loggers: list[str]
        - logs_endpoint: str | None
        - logs_protocol: str | None
    """
    # Load local environment variables first
    local_env = load_local_env()

    # Helper function to get config value with precedence
    def get_config_value(
        env_key: str, local_key: str | None = None, default: str = ""
    ) -> str:
        # 1. Environment variables have highest precedence
        env_value = os.getenv(env_key)
        if env_value is not None:
            return env_value

        # 2. Local .env file
        if local_key and local_key in local_env:
            return local_env[local_key]

        # 3. Default value
        return default

    # Get basic configuration
    enabled_str = get_config_value(
        "KIBANA_OTEL_ENABLED", "KIBANA_OTEL_ENABLED", "false"
    )
    enabled = enabled_str.lower() in ("true", "1", "yes", "on")

    service_name = get_config_value(
        "OTEL_SERVICE_NAME", "OTEL_SERVICE_NAME", "kibana-py-example"
    )

    endpoint = get_config_value(
        "OTEL_EXPORTER_OTLP_ENDPOINT",
        "OTEL_EXPORTER_OTLP_ENDPOINT",
        "http://localhost:8200",
    )

    protocol = get_config_value(
        "OTEL_EXPORTER_OTLP_PROTOCOL", "OTEL_EXPORTER_OTLP_PROTOCOL", "grpc"
    )

    # Handle authentication headers
    headers = {}

    # Get headers from environment or .env
    headers_str = get_config_value(
        "OTEL_EXPORTER_OTLP_HEADERS", "OTEL_EXPORTER_OTLP_HEADERS", ""
    )

    if headers_str:
        # Parse headers string (format: "key1=value1,key2=value2")
        # Handle quoted values
        try:
            # Remove outer quotes if present
            headers_str = headers_str.strip()
            if headers_str.startswith('"') and headers_str.endswith('"'):
                headers_str = headers_str[1:-1]

            for header_pair in headers_str.split(","):
                if "=" in header_pair:
                    key, value = header_pair.strip().split("=", 1)
                    # Expand variables in header values
                    expanded_value = _expand_variables(
                        value, {**local_env, **os.environ}
                    )
                    headers[key.strip()] = expanded_value
        except Exception as e:
            logger.warning(f"Failed to parse OTLP headers '{headers_str}': {e}")

    # If no headers configured, try to build authorization header from auth token
    if not headers:
        apm_token = get_config_value(
            "ELASTIC_APM_SECRET_TOKEN", "ELASTIC_APM_SECRET_TOKEN", ""
        )
        if apm_token:
            headers["authorization"] = f"Bearer {apm_token}"

    # Resource attributes with enhanced metadata using proper semantic conventions
    resource_attributes = {
        "service.name": service_name,
        "service.version": _get_kibana_py_version(),
        "service.language.name": "python",
        "service.language.version": _get_python_version(),
        "telemetry.sdk.name": "opentelemetry",
        "telemetry.sdk.language": "python",
        "telemetry.sdk.version": _get_opentelemetry_version(),
    }

    # Add custom resource attributes from environment
    for key, value in os.environ.items():
        if key.startswith("OTEL_RESOURCE_ATTRIBUTES_"):
            attr_name = (
                key[len("OTEL_RESOURCE_ATTRIBUTES_") :].lower().replace("_", ".")
            )
            resource_attributes[attr_name] = value

    # Log forwarding configuration — defaults to following traces (enabled)
    # unless explicitly overridden via KIBANA_OTEL_LOGS_ENABLED
    logs_enabled_str = get_config_value(
        "KIBANA_OTEL_LOGS_ENABLED", "KIBANA_OTEL_LOGS_ENABLED", ""
    )
    if logs_enabled_str:
        logs_enabled = logs_enabled_str.lower() in ("true", "1", "yes", "on")
    else:
        logs_enabled = enabled

    logs_level = get_config_value(
        "KIBANA_OTEL_LOGS_LEVEL", "KIBANA_OTEL_LOGS_LEVEL", "INFO"
    )

    # Parse logger names from comma-separated string
    logs_loggers_str = get_config_value(
        "KIBANA_OTEL_LOGS_LOGGERS", "KIBANA_OTEL_LOGS_LOGGERS", "kibana,kibana.examples"
    )
    logs_loggers = [
        name.strip() for name in logs_loggers_str.split(",") if name.strip()
    ]

    # Log-specific endpoint (optional override)
    logs_endpoint = (
        get_config_value(
            "OTEL_EXPORTER_OTLP_LOGS_ENDPOINT", "OTEL_EXPORTER_OTLP_LOGS_ENDPOINT", ""
        )
        or None
    )

    # Log-specific protocol (optional override)
    logs_protocol = (
        get_config_value(
            "OTEL_EXPORTER_OTLP_LOGS_PROTOCOL", "OTEL_EXPORTER_OTLP_LOGS_PROTOCOL", ""
        )
        or None
    )

    return {
        "enabled": enabled,
        "service_name": service_name,
        "endpoint": endpoint,
        "headers": headers,
        "protocol": protocol,
        "resource_attributes": resource_attributes,
        "logs_enabled": logs_enabled,
        "logs_level": logs_level,
        "logs_loggers": logs_loggers,
        "logs_endpoint": logs_endpoint,
        "logs_protocol": logs_protocol,
    }


def should_enable_telemetry() -> bool:
    """
    Determine if telemetry should be enabled based on configuration.

    Returns:
        True if telemetry should be enabled, False otherwise
    """
    config = get_otel_config()
    return config["enabled"]


def _get_kibana_py_version() -> str:
    """Get the kibana-py version."""
    try:
        from kibana._version import __versionstr__

        return __versionstr__
    except ImportError:
        return "unknown"


def _get_python_version() -> str:
    """Get the Python version."""
    import sys

    return f"{sys.version_info.major}.{sys.version_info.minor}.{sys.version_info.micro}"


def _get_opentelemetry_version() -> str:
    """Get the OpenTelemetry SDK version."""
    try:
        # Try to get the version from the opentelemetry-sdk package
        try:
            import importlib.metadata

            return importlib.metadata.version("opentelemetry-sdk")
        except (ImportError, Exception):
            # Fallback: try to get from opentelemetry-api
            try:
                import importlib.metadata

                return importlib.metadata.version("opentelemetry-api")
            except (ImportError, Exception):
                # Last resort: try to get from the module itself
                try:
                    from opentelemetry import __version__

                    return __version__
                except (ImportError, AttributeError):
                    return "unknown"
    except Exception:
        return "unknown"


def _validate_endpoint_format(endpoint: str) -> bool:
    """
    Validate OTLP endpoint format.

    :param endpoint: Endpoint URL to validate
    :return: True if valid, False otherwise
    """
    if not endpoint:
        return False

    # Basic URL validation
    import urllib.parse

    try:
        parsed = urllib.parse.urlparse(endpoint)
        return bool(parsed.scheme and parsed.netloc)
    except Exception:
        return False


def configure_example_telemetry(
    *, enabled: bool | None = None, logs_enabled: bool | None = None
) -> tuple[bool, bool]:
    """
    Configure OpenTelemetry for examples with OTLP integration.

    Args:
        enabled: Override telemetry enable/disable (None = auto-detect)
        logs_enabled: Override log forwarding enable/disable (None = auto-detect)

    Returns:
        Tuple of (traces_configured, logs_configured)
    """
    try:
        # Import here to avoid dependency issues
        from kibana.observability import configure_opentelemetry
    except ImportError:
        logger.debug(
            "OpenTelemetry not available. Install with: pip install kibana-py[observability]"
        )
        return False, False

    # Get configuration
    config = get_otel_config()

    # Override enabled states if provided
    if enabled is not None:
        config["enabled"] = enabled
    if logs_enabled is not None:
        config["logs_enabled"] = logs_enabled

    # If traces disabled, don't configure anything
    if not config["enabled"]:
        logger.debug("OpenTelemetry telemetry disabled")
        return False, False

    # Validate configuration
    validation_errors = _validate_otel_config(config)
    if validation_errors:
        for error in validation_errors:
            logger.warning(f"OpenTelemetry configuration error: {error}")
        return False, False

    try:
        # Test OTLP endpoint connectivity if possible
        if not _test_apm_connectivity(config["endpoint"], config["headers"]):
            logger.warning(
                f"OTLP endpoint not reachable at {config['endpoint']}, telemetry may not work"
            )
            # Continue anyway - server might be starting up

        # Prepare log forwarding parameters
        logs_config = {}
        if config["logs_enabled"]:
            logs_config["logs_enabled"] = True
            logs_config["logs_level"] = config["logs_level"]
            logs_config["logs_loggers"] = config["logs_loggers"]

            # Use log-specific endpoint if configured, otherwise don't pass it (inherit from traces)
            if config["logs_endpoint"]:
                logs_config["logs_endpoint"] = config["logs_endpoint"]

            # Use log-specific protocol if configured, otherwise don't pass it (inherit from traces)
            if config["logs_protocol"]:
                logs_config["logs_protocol"] = config["logs_protocol"]

        # Configure OpenTelemetry with log forwarding support
        configure_opentelemetry(
            enabled=True,
            service_name=config["service_name"],
            exporter="otlp",
            endpoint=config["endpoint"],
            headers=config["headers"],
            protocol=config["protocol"],
            **logs_config,
        )

        traces_configured = True
        logs_configured = config["logs_enabled"]

        if logs_configured:
            logger.info(
                f"OpenTelemetry configured with traces and logs for OTLP endpoint: {config['endpoint']}"
            )
        else:
            logger.info(
                f"OpenTelemetry configured with traces only for OTLP endpoint: {config['endpoint']}"
            )

        return traces_configured, logs_configured

    except Exception as e:
        logger.error(f"Failed to configure OpenTelemetry: {e}")
        return False, False


def _validate_log_level(log_level: str) -> bool:
    """
    Validate log level string.

    :param log_level: Log level to validate
    :return: True if valid, False otherwise
    """
    valid_levels = ["DEBUG", "INFO", "WARNING", "ERROR", "CRITICAL"]
    return log_level.upper() in valid_levels


def _validate_logger_names(logger_names: list[str]) -> list[str]:
    """
    Validate logger names and return any invalid ones.

    :param logger_names: List of logger names to validate
    :return: List of invalid logger names
    """
    invalid_names = []

    for name in logger_names:
        # Logger names should be non-empty strings
        if not name or not isinstance(name, str):
            invalid_names.append(str(name))
            continue

        # Logger names should not contain invalid characters
        # Allow alphanumeric, dots, underscores, and hyphens
        import re

        if not re.match(r"^[a-zA-Z0-9._-]+$", name):
            invalid_names.append(name)

    return invalid_names


def _validate_otel_config(config: dict[str, Any]) -> list[str]:
    """
    Validate OpenTelemetry configuration.

    :param config: Configuration dictionary
    :return: List of validation error messages
    """
    errors = []

    # Validate endpoint format
    if not _validate_endpoint_format(config["endpoint"]):
        errors.append(f"Invalid OTLP endpoint format: {config['endpoint']}")

    # Validate service name
    if not config["service_name"] or not config["service_name"].strip():
        errors.append("Service name cannot be empty")

    # Validate protocol
    valid_protocols = ["grpc", "http/protobuf"]
    if config["protocol"] not in valid_protocols:
        errors.append(
            f"Invalid protocol '{config['protocol']}', must be one of: {valid_protocols}"
        )

    # Validate log forwarding configuration if enabled
    if config.get("logs_enabled", False):
        # Validate log level
        if not _validate_log_level(config.get("logs_level", "")):
            errors.append(
                f"Invalid log level '{config.get('logs_level')}', must be one of: DEBUG, INFO, WARNING, ERROR, CRITICAL"
            )

        # Validate logger names
        invalid_loggers = _validate_logger_names(config.get("logs_loggers", []))
        if invalid_loggers:
            errors.append(f"Invalid logger names: {invalid_loggers}")

        # Validate logs endpoint if specified
        logs_endpoint = config.get("logs_endpoint")
        if logs_endpoint and not _validate_endpoint_format(logs_endpoint):
            errors.append(f"Invalid logs endpoint format: {logs_endpoint}")

        # Validate logs protocol if specified
        logs_protocol = config.get("logs_protocol")
        if logs_protocol and logs_protocol not in valid_protocols:
            errors.append(
                f"Invalid logs protocol '{logs_protocol}', must be one of: {valid_protocols}"
            )

    return errors


def _test_apm_connectivity(endpoint: str, headers: dict[str, str]) -> bool:
    """
    Test connectivity to OTLP endpoint.

    :param endpoint: OTLP endpoint
    :param headers: Authentication headers
    :return: True if server is reachable, False otherwise
    """
    try:
        import urllib.parse
        import urllib.request

        # Parse endpoint to get base URL
        parsed = urllib.parse.urlparse(endpoint)

        # For gRPC endpoints, we can't easily test connectivity
        # For HTTP endpoints, we could try a health check
        if parsed.scheme in ("http", "https"):
            # Try to connect with a short timeout
            req = urllib.request.Request(f"{parsed.scheme}://{parsed.netloc}/")

            # Add headers if provided
            for key, value in headers.items():
                req.add_header(key, value)

            with urllib.request.urlopen(req, timeout=2):
                return True
        else:
            # For gRPC or other protocols, assume connectivity
            return True

    except Exception as e:
        logger.debug(f"OTLP connectivity test failed: {e}")
        return False


def print_telemetry_info() -> None:
    """Print telemetry configuration information with masked tokens."""
    config = get_otel_config()

    print("=== Telemetry Configuration ===")
    print(f"Traces Enabled: {config['enabled']}")
    print(f"Service Name: {config['service_name']}")
    print(f"OTLP Endpoint: {config['endpoint']}")
    print(f"Protocol: {config['protocol']}")

    # Log forwarding configuration
    print(f"Log Forwarding Enabled: {config['logs_enabled']}")
    if config["logs_enabled"]:
        print(f"Log Level: {config['logs_level']}")
        print(f"Log Loggers: {', '.join(config['logs_loggers'])}")

        if config["logs_endpoint"]:
            print(f"Logs Endpoint: {config['logs_endpoint']}")
        else:
            print("Logs Endpoint: (inherits from traces)")

        if config["logs_protocol"]:
            print(f"Logs Protocol: {config['logs_protocol']}")
        else:
            print("Logs Protocol: (inherits from traces)")

    # Display headers with masked tokens
    if config["headers"]:
        print("Headers:")
        for key, value in config["headers"].items():
            if key.lower() in ("authorization", "auth", "token"):
                # Mask sensitive values - show first 10 and last 4 characters
                if len(value) > 14:
                    masked_value = f"{value[:10]}...{value[-4:]}"
                else:
                    masked_value = "***"
                print(f"  {key}: {masked_value}")
            else:
                print(f"  {key}: {value}")
    else:
        print("Headers: None")

    # Check if .env file exists
    env_file = Path(__file__).parent.parent / "elastic-start-local" / ".env"
    if env_file.exists():
        print(f"✅ Local .env file found: {env_file}")
    else:
        print(f"❌ Local .env file not found: {env_file}")

    print("=" * 32)
    print()


def cleanup_telemetry() -> None:
    """
    Implement automatic cleanup on example exit.
    Ensure proper OpenTelemetry shutdown sequence.
    Handle cleanup errors gracefully.
    """
    try:
        # Import here to avoid dependency issues
        from opentelemetry import trace

        # Get the current tracer provider
        tracer_provider = trace.get_tracer_provider()

        # If it's an SDK tracer provider, shut it down properly
        if hasattr(tracer_provider, "shutdown"):
            logger.debug("Shutting down OpenTelemetry tracer provider")
            tracer_provider.shutdown()

    except ImportError:
        # OpenTelemetry not available, nothing to clean up
        logger.debug("OpenTelemetry not available, no cleanup needed")
    except Exception as e:
        # Handle cleanup errors gracefully - never fail the example
        logger.debug(f"Error during telemetry cleanup: {e}")


def setup_telemetry_cleanup() -> None:
    """
    Set up automatic telemetry cleanup on example exit.
    Registers cleanup function to run when the process exits.
    """
    import atexit

    # Register cleanup function to run on exit
    atexit.register(cleanup_telemetry)


def should_cleanup(prompt: str = "Delete created resources? (y/N): ") -> bool:
    """
    Determine whether to clean up resources created by an example.

    When ``--cleanup`` is passed on the command line, resources are deleted
    automatically.  When ``--no-cleanup`` is passed, resources are kept.
    If neither flag is present the user is prompted interactively.

    :param prompt: Message shown when prompting interactively
    :return: True if resources should be deleted
    """
    if "--cleanup" in sys.argv:
        return True
    if "--no-cleanup" in sys.argv:
        return False
    return input(prompt).lower().strip() == "y"


